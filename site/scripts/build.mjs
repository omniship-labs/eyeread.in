#!/usr/bin/env node
/**
 * build.mjs — assemble the deployable marketing site.
 *
 * The site source (index.html, css/*.css, js/*.js) is hand-written and lives
 * in `site/`. Everything it BORROWS from elsewhere in the repo — the design
 * tokens and brand assets — is generated/copied here at build time rather than
 * committed, so there is exactly one source of truth and nothing to drift.
 *
 * Outputs (all git-ignored, recreated on every run):
 *   site/css/tokens.css       ← concatenated from design/tokens/
 *   site/assets/logos/*.svg   ← copied from design/assets/logos/
 *   site/assets/og-image.svg  ← copied from design/assets/brand/
 *   site/assets/favicon.svg   ← copied from public/
 *
 * Run `npm run site:build` locally before serving, or let the deploy workflow
 * run it before publishing `site/` to GitHub Pages.
 */
import { readFile, writeFile, mkdir, cp } from 'node:fs/promises';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const HERE = dirname(fileURLToPath(import.meta.url));
const ROOT = resolve(HERE, '..', '..');
const SITE = resolve(ROOT, 'site');

/* ── 1. Design tokens → site/css/tokens.css ──────────────────────────────
   Order matters: fonts (with the Google Fonts @import) first, then the
   variable layers. We skip base.css — the site ships its own reset/base. */
const TOKEN_FILES = ['fonts.css', 'colors.css', 'typography.css', 'spacing.css', 'effects.css'];

async function buildTokens() {
  const header = `/* ============================================================
   eyeread.in · marketing site — design tokens
   GENERATED by site/scripts/build.mjs — do not edit, do not commit.
   Source: design/tokens/{${TOKEN_FILES.join(', ')}}
   ============================================================ */\n\n`;

  const parts = [];
  for (const file of TOKEN_FILES) {
    const css = await readFile(resolve(ROOT, 'design', 'tokens', file), 'utf8');
    parts.push(`/* ---- from design/tokens/${file} ---- */\n${css.trim()}\n`);
  }

  const out = resolve(SITE, 'css', 'tokens.css');
  await writeFile(out, header + parts.join('\n') + '\n');
  console.log(`[site:build] tokens.css ← ${TOKEN_FILES.length} token file(s)`);
}

/* ── 2. Brand assets → site/assets/ ──────────────────────────────────────── */
async function copyAssets() {
  await mkdir(resolve(SITE, 'assets'), { recursive: true });

  // Whole logo set (small SVGs) so config can point at any of them.
  await cp(resolve(ROOT, 'design', 'assets', 'logos'), resolve(SITE, 'assets', 'logos'), {
    recursive: true,
  });

  const files = [
    [resolve(ROOT, 'design', 'assets', 'brand', 'og-image.svg'), resolve(SITE, 'assets', 'og-image.svg')],
    [resolve(ROOT, 'public', 'favicon.svg'), resolve(SITE, 'assets', 'favicon.svg')],
  ];
  for (const [from, to] of files) await cp(from, to);

  console.log('[site:build] assets ← design/assets/logos, design/assets/brand, public/favicon.svg');
}

await buildTokens();
await copyAssets();
console.log('[site:build] done');
